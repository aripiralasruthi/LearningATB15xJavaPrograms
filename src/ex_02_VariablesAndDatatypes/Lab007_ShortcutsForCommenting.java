public class Lab007_ShortcutsForCommenting {
    public static void main(String[] args) {
//        shortcut keys for single line comment is ctrl + /
        /* shortcut for multi line comment is ctrl + shift + /
        */
        System.out.println("\n Shortcut keys for single line comment is ctrl + /");
        System.out.println("\n /* Shortcut for \n" +
                "   multi line comment is ctrl + shift + /\n" +
                "   */");
    }
}
